using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace SwitchCase
{
    class Program
    {
        static void Main(string[] args)
        {
            int i = 2;

            switch(i)
                {

                case 1:
                    Console.WriteLine("one");
                    break;

                    case 2:
                    Console.WriteLine("two");
                    break;

                    case 3:
                    Console.WriteLine("three");
                    break;


            }

            Console.ReadLine();

        }
    }
}
